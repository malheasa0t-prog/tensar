    function extractErrorCode(message) {
        var match = String(message || '').match(ERROR_CODE_PATTERN);
        return match ? match[1] : '';
    }

    function formatError(code, message) {
        var normalizedCode = String(code || '').trim().toUpperCase();
        var normalizedMessage = String(message || ERROR_CODES[normalizedCode] || DEFAULT_ERROR_MESSAGE).trim();

        if (extractErrorCode(normalizedMessage)) {
            return normalizedMessage;
        }

        return normalizedCode ? '[' + normalizedCode + '] ' + normalizedMessage : normalizedMessage;
    }

    function getCustomerSafeMessage(code) {
        return 'حدث خطأ (رمز: ' + String(code || '').trim().toUpperCase() + '). تواصل مع الدعم.';
    }

    function logError(code, message, error) {
        console.error(formatError(code, message), error);
    }

    window.AdminErrorCodes = {
        ERROR_CODES: ERROR_CODES,
        extractErrorCode: extractErrorCode,
        formatError: formatError,
        getCustomerSafeMessage: getCustomerSafeMessage,
        logError: logError
    };
